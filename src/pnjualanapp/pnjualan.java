    private void tampilBeli() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListDPJ.clear();
        ListDPJ = pc.cariBarang(nonota.getText());
        for (int x = 0; x < ListDPJ.size(); x++) {
            Object[] data = new Object[5];
            data[0] = ListDPJ.get(x).getkdbarang();
            data[1] = ListDPJ.get(x).getnmbarang();
            data[2] = ListDPJ.get(x).getharga_jual();
            data[3] = ListDPJ.get(x).getJmlbeli();
            data[4] = ListDPJ.get(x).getTbayar();
            model.addRow(data);
        }
    }

