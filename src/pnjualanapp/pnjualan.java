    private void txtcari1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcari1KeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListBarang.clear();
        ListBarang = cb.cariBarang(txtcari1.getText());
        for (int x = 0; x < ListBarang.size(); x++) {
            Object[] data = new Object[5];
            data[0] = ListBarang.get(x).getkdbarang();
            data[1] = ListBarang.get(x).getnmbarang();
            data[2] = ListBarang.get(x).getsatuan();
            data[3] = ListBarang.get(x).getstok();
            data[4] = ListBarang.get(x).getharga_beli();
            model.addRow(data);
        }
    }//GEN-LAST:event_txtcari1KeyReleased

    private void btntambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambah1ActionPerformed
        // TODO add your handling code here:
        if (kdbarang.getText().equals("") || nonota.getText().equals("") || nmbarang.getText().equals("") || nama_pembeli.getText().equals("")
                || hargajual.getText().equals("") || jmlhbeli.getText().equals("") || ttotal.getText().equals("") || ttunai.getText().equals("")
                || tkembali.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Data Harus Lengkap");
        } else {
            int jml = Integer.parseInt(jmlhbeli.getText());
            int stok = Integer.parseInt(nonota1.getText());
            if (jml > stok) {
                JOptionPane.showMessageDialog(this, "stok obat tidak cukup");
            } else {
                ListDb = pc.CekBarang(nonota.getText(), kdbarang.getText());
                if (ListDb.size() > 0) {
                    JOptionPane.showMessageDialog(this, "Barang ini sudah Anda beli, silahkan pilih yang lain");
                } else {
                    int harga = Integer.parseInt(hargajual.getText());
                    DataPenjualan dp = new DataPenjualan();
                    dp.setNonota(nonota.getText());
                    dp.setkdbarang(kdbarang.getText());
                    dp.setnmbarang(nmbarang.getText());
                    dp.setharga_jual(harga);
                    dp.setJmlbeli(jml);
                    dp.setTbayar(harga * jml);
                    if (pc.tambahpenjualan(dp) == 0) {
                        JOptionPane.showMessageDialog(this, "Data Gagal disimpan");
                    } else {
                        int sisaStok = stok - jml;
                        cb.updateStock(stok, kdbarang.getText());
                        buatTablebeli();
                        tampilBeli();
                        int total = Integer.parseInt(ttotal.getText());
                        int totalBayar = total + (harga * jml);
                        ttotal.setText(Integer.toString(totalBayar));
                        kdbarang.setText(null);
                        nmbarang.setText("");
                        hargajual.setText(null);
                        jmlhbeli.setText(null);
                        ttunai.setText("0");
                        tkembali.setText("0");
                    }
                }
            }
        }
    }//GEN-LAST:event_btntambah1ActionPerformed

    private void ttunaiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ttunaiKeyPressed
        if (evt.getKeyCode() == 10) {
            int total = Integer.parseInt(ttotal.getText());
            int bayar = Integer.parseInt(ttunai.getText());
            if (bayar < total) {
                JOptionPane.showMessageDialog(this, "Jumlah uang tidak cukup"); } else {
                int kembali = bayar - total;
                tkembali.setText(Integer.toString(kembali));} }
    }//GEN-LAST:event_ttunaiKeyPressed

