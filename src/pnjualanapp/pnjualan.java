    private void buatNoNota() {
        ListDb = bc.tampil();
        if (ListDb.isEmpty()) {
            nonota.setText("N-" + 1);
        } else {
            int a = ListDb.size() - 1;
            int no = Integer.parseInt(ListDb.get(a).getnonota().replace("N-", "")) + 1;
            nonota.setText("No-" + no);
            nonota.setEnabled(false);
        }
    }

